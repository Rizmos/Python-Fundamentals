


first = float(input("Enter first number"))
second = float(input("Enter second number"))
operator= input("enter operator")

if operator == "+" :
    print(first + second)

elif operator == "-" :
    print(first - second)

elif operator== "*":
    print(first * second)

elif operator == "/":
    print(first / second)

else:
    print("INVALID INPUT")

