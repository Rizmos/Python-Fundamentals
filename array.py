
courses = ["MIT","Cyber-security","Datascience"]

print(courses)


#Adding a new element in an array

courses.append("Python")
print(courses)

#Removing an element in an array

courses.remove("Datascience")
print(courses)

#Accessing an element from an array(Know the index position)

print(courses[1])

#Looping an array

for course in courses:
    print(course)


